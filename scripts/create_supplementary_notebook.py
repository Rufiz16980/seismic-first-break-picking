# === Section 6: SNR Proxy Redefinition ===
cells.append(md_cell([
    '## Section 6 — SNR Proxy Redefinition and Visualization Example Reclassification\n',
    '\n',
    'Define SNR explicitly as `RMS(post_arrival_window) / RMS(pre_arrival_window)` using a fixed\n',
    'window size (50ms each side of the label). Recompute per asset.\n',
]))

cells.append(code_cell([
    '# ============================================================\n',
    '# Cell 7 — Section 6: SNR Redefinition + Reclassification\n',
    '# ============================================================\n',
    '\n',
    'def compute_rms_snr(trace, fb_sample, n_samples, window_samples=25):\n',
    '    """Compute SNR as RMS(post) / RMS(pre) with fixed window."""\n',
    '    pre_start = max(0, fb_sample - window_samples)\n',
    '    pre_end = fb_sample\n',
    '    post_start = fb_sample\n',
    '    post_end = min(n_samples, fb_sample + window_samples)\n',
    '    \n',
    '    if pre_end <= pre_start or post_end <= post_start:\n',
    '        return None\n',
    '    \n',
    '    pre = trace[pre_start:pre_end]\n',
    '    post = trace[post_start:post_end]\n',
    '    \n',
    '    rms_pre = np.sqrt(np.mean(pre**2))\n',
    '    rms_post = np.sqrt(np.mean(post**2))\n',
    '    \n',
    '    if rms_pre < 1e-15:\n',
    '        return None\n',
    '    \n',
    '    return rms_post / rms_pre\n',
    '\n',
    '\n',
    'def compute_per_shot_snr(grp, shot_stats, samp_rate_us, n_sample_per_shot=50):\n',
    '    """Compute median SNR per shot using RMS definition."""\n',
    '    sort_idx = shot_stats["_sort_idx"]\n',
    '    split_points = shot_stats["_split_points"]\n',
    '    unique_shots = shot_stats["_unique_shots"]\n',
    '    spare1 = shot_stats["_spare1"]\n',
    '    \n',
    '    data_ds = grp["data_array"]\n',
    '    n_traces, n_samples = data_ds.shape\n',
    '    dt_ms = samp_rate_us / 1000.0\n',
    '    window_ms = 50\n',
    '    window_samples = int(window_ms / dt_ms)\n',
    '    \n',
    '    per_shot_snr = {}\n',
    '    \n',
    '    for shot_i in range(len(unique_shots)):\n',
    '        s, e = split_points[shot_i], split_points[shot_i + 1]\n',
    '        tidx = sort_idx[s:e]\n',
    '        fb = spare1[tidx].astype(np.float64)\n',
    '        \n',
    '        labeled_mask = fb > 0\n',
    '        n_labeled = int(labeled_mask.sum())\n',
    '        \n',
    '        if n_labeled < 5:\n',
    '            per_shot_snr[shot_i] = None\n',
    '            continue\n',
    '        \n',
    '        labeled_local_idx = np.where(labeled_mask)[0]\n',
    '        sample_size = min(n_sample_per_shot, n_labeled)\n',
    '        chosen = rng.choice(labeled_local_idx, size=sample_size, replace=False)\n',
    '        \n',
    '        snr_vals = []\n',
    '        for ci in chosen:\n',
    '            global_idx = int(tidx[ci])\n',
    '            trace = data_ds[global_idx].astype(np.float64)\n',
    '            fb_ms = fb[ci]\n',
    '            fb_sample = int(fb_ms / dt_ms)\n',
    '            snr = compute_rms_snr(trace, fb_sample, n_samples, window_samples)\n',
    '            if snr is not None:\n',
    '                snr_vals.append(snr)\n',
    '        \n',
    '        if snr_vals:\n',
    '            per_shot_snr[shot_i] = float(np.median(snr_vals))\n',
    '        else:\n',
    '            per_shot_snr[shot_i] = None\n',
    '    \n',
    '    return per_shot_snr\n',
    '\n',
    'print("SNR definition: RMS(post_50ms_window) / RMS(pre_50ms_window)")\n',
    'print("This is a ratio, not log-transformed.")\n',
    'print("Values > 1 indicate signal presence; higher = cleaner.")\n',
    'print("\\nComputing per-shot SNR for all assets...")\n',
    'print("(This may take several minutes per asset.)")\n',
]))

