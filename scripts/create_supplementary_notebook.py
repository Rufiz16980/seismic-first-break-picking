    '            result["diff_mean"] = float(np.mean(diffs))\n',
    '            result["diff_std"] = float(np.std(diffs))\n',
    '            result["diff_median"] = float(np.median(diffs))\n',
    '            result["diff_abs_max"] = float(np.max(abs_diffs))\n',
    '            result["n_exact_match"] = int(np.sum(diffs == 0))\n',
    '            \n',
    '            plot_n = min(50000, n_both)\n',
    '            plot_idx = rng.choice(n_both, size=plot_n, replace=False)\n',
    '            \n',
    '            axes[i].hist(diffs[plot_idx], bins=150, color="teal", edgecolor="none", alpha=0.8)\n',
    '            axes[i].set_title(f"{name}: SPARE1 - MBT\\nMAE={result[\'mae\']:.1f}ms, n={n_both:,}")\n',
    '            axes[i].set_xlabel("Difference (ms)")\n',
    '            axes[i].set_ylabel("Count")\n',
