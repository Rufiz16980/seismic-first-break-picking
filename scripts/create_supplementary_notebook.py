    '        result["mbt_shape"] = list(grp["MODELLED_BREAK_TIME"].shape)\n',
    '        \n',
    '        # Basic stats of MBT\n',
    '        mbt_zero = int(np.sum(mbt == 0))\n',
    '        mbt_pos = int(np.sum(mbt > 0))\n',
    '        mbt_neg = int(np.sum(mbt < 0))\n',
    '        result["mbt_count_zero"] = mbt_zero\n',
    '        result["mbt_count_positive"] = mbt_pos\n',
    '        result["mbt_count_negative"] = mbt_neg\n',
    '        result["mbt_total"] = len(mbt)\n',
    '        \n',
    '        # Compare with SPARE1 where both are positive\n',
    '        both_pos = (spare1 > 0) & (mbt > 0)\n',
