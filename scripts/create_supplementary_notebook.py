# === Section 1: FIRST_BREAK_TIME vs SPARE1 ===
cells.append(md_cell([
    '## Section 1 — FIRST_BREAK_TIME vs SPARE1 Comparison\n',
    '\n',
    'For each asset, load both FIRST_BREAK_TIME and SPARE1 for a random sample of 5000 labeled traces.\n',
    'Compute the difference. Plot a histogram of the differences.\n',
    'If they are identical, document that SPARE1 is confirmed as the sole ground truth field.\n',
    'If they differ, document how and by how much.\n',
    '\n',
    '**Key context:** Phase 0.5 found `n_both_labeled: 0` for all assets, meaning that for every asset,\n',
    'the existing comparison function found zero traces where both SPARE1 > 0 AND FIRST_BREAK_TIME > 0.\n',
    'This section investigates WHY that happened and whether FIRST_BREAK_TIME contains anything useful.\n',
]))

cells.append(code_cell([
    '# ============================================================\n',
    '# Cell 2 — Section 1: FIRST_BREAK_TIME vs SPARE1\n',
    '# ============================================================\n',
    'handles = open_all_assets()\n',
    'section1_results = {}\n',
    '\n',
    'N_SAMPLE = 5000\n',
    'rng = np.random.RandomState(42)\n',
    '\n',
    'fig, axes = plt.subplots(2, 2, figsize=(16, 12))\n',
    'axes = axes.ravel()\n',
    '\n',
    'for i, name in enumerate(ASSET_NAMES):\n',
    '    grp = handles[name]["grp"]\n',
    '    meta = handles[name]["meta"]\n',
    '    \n',
    '    spare1 = grp["SPARE1"][:].ravel().astype(np.float64)\n',
    '    \n',
    '    result = {"asset": name}\n',
    '    \n',
    '    if "FIRST_BREAK_TIME" in grp:\n',
    '        fbt = grp["FIRST_BREAK_TIME"][:].ravel().astype(np.float64)\n',
    '        result["fbt_present"] = True\n',
    '        result["fbt_shape"] = list(grp["FIRST_BREAK_TIME"].shape)\n',
    '        result["fbt_dtype"] = str(grp["FIRST_BREAK_TIME"].dtype)\n',
    '        result["spare1_shape"] = list(grp["SPARE1"].shape)\n',
    '        result["spare1_dtype"] = str(grp["SPARE1"].dtype)\n',
    '        \n',
    '        # Characterize FIRST_BREAK_TIME values\n',
    '        fbt_zero = int(np.sum(fbt == 0))\n',
    '        fbt_neg = int(np.sum(fbt < 0))\n',
    '        fbt_pos = int(np.sum(fbt > 0))\n',
    '        result["fbt_count_zero"] = fbt_zero\n',
    '        result["fbt_count_negative"] = fbt_neg\n',
    '        result["fbt_count_positive"] = fbt_pos\n',
    '        result["fbt_total"] = len(fbt)\n',
    '        \n',
    '        # Characterize SPARE1 values\n',
    '        s1_zero = int(np.sum(spare1 == 0))\n',
    '        s1_neg = int(np.sum(spare1 < 0))\n',
    '        s1_pos = int(np.sum(spare1 > 0))\n',
    '        result["spare1_count_zero"] = s1_zero\n',
    '        result["spare1_count_negative"] = s1_neg\n',
    '        result["spare1_count_positive"] = s1_pos\n',
    '        result["spare1_total"] = len(spare1)\n',
    '        \n',
    '        # Check overlap: where both are positive (labeled)\n',
    '        both_pos_mask = (spare1 > 0) & (fbt > 0)\n',
    '        n_both_pos = int(both_pos_mask.sum())\n',
    '        result["n_both_positive"] = n_both_pos\n',
    '        \n',
    '        # If no overlap, check if FBT is always 0 or always matches SPARE1\n',
    '        labeled_mask = spare1 > 0\n',
    '        n_labeled = int(labeled_mask.sum())\n',
    '        \n',
    '        if n_both_pos > 0:\n',
    '            # Sample from traces where both are positive\n',
    '            both_idx = np.where(both_pos_mask)[0]\n',
    '            sample_idx = rng.choice(both_idx, size=min(N_SAMPLE, len(both_idx)), replace=False)\n',
    '            diffs = spare1[sample_idx] - fbt[sample_idx]\n',
    '            result["diff_mean"] = float(np.mean(diffs))\n',
    '            result["diff_std"] = float(np.std(diffs))\n',
    '            result["diff_abs_max"] = float(np.max(np.abs(diffs)))\n',
    '            result["diff_median"] = float(np.median(diffs))\n',
    '            result["n_exact_match"] = int(np.sum(diffs == 0))\n',
    '            result["exact_match_fraction"] = float(np.sum(diffs == 0) / len(diffs))\n',
    '            \n',
    '            axes[i].hist(diffs, bins=100, color="steelblue", edgecolor="none", alpha=0.8)\n',
    '            axes[i].set_title(f"{name}: SPARE1 - FIRST_BREAK_TIME (n={len(diffs):,})")\n',
    '            axes[i].set_xlabel("Difference (ms)")\n',
    '            axes[i].set_ylabel("Count")\n',
    '        else:\n',
    '            # Investigate what FBT values look like for labeled traces\n',
    '            if n_labeled > 0:\n',
    '                fbt_at_labeled = fbt[labeled_mask]\n',
    '                result["fbt_at_labeled_traces"] = {\n',
    '                    "all_zero": bool(np.all(fbt_at_labeled == 0)),\n',
    '                    "pct_zero": float(np.sum(fbt_at_labeled == 0) / len(fbt_at_labeled) * 100),\n',
    '                    "pct_positive": float(np.sum(fbt_at_labeled > 0) / len(fbt_at_labeled) * 100),\n',
    '                    "pct_negative": float(np.sum(fbt_at_labeled < 0) / len(fbt_at_labeled) * 100),\n',
    '                    "unique_values_sample": sorted(set(fbt_at_labeled[:1000].tolist())),\n',
    '                }\n',
    '                \n',
    '                # Also check: maybe FBT uses a different convention\n',
    '                fbt_nonzero = fbt[fbt != 0]\n',
    '                if len(fbt_nonzero) > 0:\n',
    '                    result["fbt_nonzero_stats"] = {\n',
    '                        "min": float(np.min(fbt_nonzero)),\n',
    '                        "max": float(np.max(fbt_nonzero)),\n',
    '                        "mean": float(np.mean(fbt_nonzero)),\n',
    '                        "n_total": int(len(fbt_nonzero)),\n',
    '                    }\n',
    '                else:\n',
    '                    result["fbt_nonzero_stats"] = "ALL ZERO"\n',
    '            \n',
    '            axes[i].text(0.5, 0.5, f"{name}\\nNo overlap between\\nSPARE1>0 and FBT>0\\n"\n',
    '                        f"FBT positive: {fbt_pos:,}\\nSPARE1 positive: {s1_pos:,}",\n',
    '                        transform=axes[i].transAxes, ha="center", va="center", fontsize=12)\n',
    '            axes[i].set_title(f"{name}: FIRST_BREAK_TIME Analysis")\n',
    '    else:\n',
    '        result["fbt_present"] = False\n',
    '        axes[i].text(0.5, 0.5, f"{name}\\nFIRST_BREAK_TIME not present",\n',
    '                    transform=axes[i].transAxes, ha="center", va="center", fontsize=12)\n',
    '        axes[i].set_title(f"{name}: FIRST_BREAK_TIME")\n',
    '    \n',
    '    section1_results[name] = result\n',
    '    print(f"\\n=== {name} ===")\n',
    '    for k, v in result.items():\n',
    '        if k != "asset":\n',
    '            print(f"  {k}: {v}")\n',
    '\n',
    'fig.suptitle("Section 1: FIRST_BREAK_TIME vs SPARE1", fontsize=14, fontweight="bold")\n',
    'fig.tight_layout()\n',
    'save_path = os.path.join(SUPP_PLOT_DIR, "sec1_fbt_vs_spare1.png")\n',
    'fig.savefig(save_path, dpi=150, bbox_inches="tight")\n',
    'plt.show()\n',
    'print(f"\\nSaved: {save_path}")\n',
    '\n',
    'supp_report["section_1_fbt_vs_spare1"] = section1_results\n',
]))

